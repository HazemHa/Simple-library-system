 @Override
    public void OnCompete(Message msg) {
        // show a joptionpane dialog using showMessageDialog
        if (msg.getContent() != null) {
            int id = (int) msg.getContent();
            newUser.setId(id);
            LoadedData.getInstance().setCurrentUser(newUser);
            JOptionPane.showMessageDialog(this, msg.getMessage(), "Compete", JOptionPane.INFORMATION_MESSAGE);
            new HomePage3().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Something happend " + msg.getMessage(), "Compete", JOptionPane.WARNING_MESSAGE);

        }
    }

