    private void WriteInFile(String content) {
        BufferedWriter bw = null;
        FileWriter fw = null;
        try {
            
            File file = new File("/users.txt");
            if (!file.exists()) {
file.createNewFile();
}
            fw = new FileWriter(file);
            bw = new BufferedWriter(fw);
            bw.write(content);

        } catch (IOException ex) {
                   JOptionPane.showMessageDialog(this,"can not wirte to file :"+ex.getLocalizedMessage(), "Failure", JOptionPane.ERROR_MESSAGE);

        } finally {
            if (bw != null) {
                try {
                    bw.close();
                } catch (IOException ex) {
                   JOptionPane.showMessageDialog(this,"can not wirte to file :"+ ex.getLocalizedMessage(), "Failure", JOptionPane.ERROR_MESSAGE);
                }
            }

            if (fw != null) {
                try {
                    fw.close();
                } catch (IOException ex) {
                   JOptionPane.showMessageDialog(this,"can not wirte to file :"+ ex.getLocalizedMessage(), "Failure", JOptionPane.ERROR_MESSAGE);
                }
            }

        }

    }
    
    

